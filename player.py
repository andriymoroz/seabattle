from board import Board

class Player:
    def __init__(self):
        self.name = "Player" + chr(randint(0, 9))
        self.board = Board()
        pass

    def init(self, size):
        # set up board, etc
        self.board.init(size)

        pass

    def get_move(self, prev_res):
        return 1, 1

    def set_move(self, x, y):
        if self.board[x][y] == 'X':
            return "inj"
        else:
            return "miss"



    def print_status(self):
        pass

    def has_ships(self):
        size = self.board.get_size()
        for x in range(size):
            for y in range(size):
                if self.board.get_cell(self.board.OWN, x, y) == "X":
                    return True

        return False
